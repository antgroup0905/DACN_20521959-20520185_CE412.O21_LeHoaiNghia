# Initialize sensor values
pHvalue = 0.0
turbidity = 0.0
temperature = 0.0

def on_connect(client, userdata, flags, rc):
    if rc == 0:
        print("Connected successfully to MQTT broker")
        # Subscribe to the topics
        client.subscribe("sensor_data_topic/pH")
        client.subscribe("sensor_data_topic/Turbidity")
        client.subscribe("sensor_data_topic/Temperature")
    else:
        print(f"Connection failed with code {rc}")

def on_message(client, userdata, message):
    global pHvalue, turbidity, temperature
    data = message.payload.decode()
    if message.topic == "sensor_data_topic/pH":
        pHvalue = float(data)
    elif message.topic == "sensor_data_topic/Turbidity":
        turbidity = float(data)
    elif message.topic == "sensor_data_topic/Temperature":
        temperature = float(data)

client = mqtt.Client()
client.on_connect = on_connect
client.on_message = on_message

# Configure TLS/SSL
client.tls_set(ca_certs=None, certfile=None, keyfile=None, cert_reqs=mqtt.ssl.CERT_REQUIRED,
               tls_version=mqtt.ssl.PROTOCOL_TLS, ciphers=None)
client.tls_insecure_set(False)  # Ensures that the server certificate is verified
client.username_pw_set(mqtt_username, mqtt_password)
client.connect(broker_address, broker_port)

@app.route("/")
def index():
    return render_template("index.html")

@app.route("/api/data")
def get_data():
    return jsonify({
        "pHvalue": pHvalue,
        "turbidity": turbidity,
        "temperature": temperature
    })

if __name__ == "__main__":
    client.loop_start()
    app.run(debug=True)
